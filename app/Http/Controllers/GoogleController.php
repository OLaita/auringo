    public function googleLogin(Request $request){

        $user = User::where('provider_id', $request->id)->first();
        $user->update([
            'name'=> $request->name,
            'surname'=> $request->surname,
            'username'=>$request->username,
            'country'=>$request->country,
            'rol_id'=>2
        ]);

        Auth::login($user);
        return redirect()->intended('home');

